#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn default_12d_constructs_without_error() {
        assert!(HawkesLobSimulator::default_12d().is_ok());
    }

    #[test]
    fn reset_initialises_book_with_both_sides() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 10.0;
        sim.reset(42);
        assert!(sim.book.best_bid().is_some(), "no bids after reset");
        assert!(sim.book.best_ask().is_some(), "no asks after reset");
    }

    #[test]
    fn simulate_trading_day_produces_events_and_trades() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        // Use short window for test speed.
        sim.config.t_max = 60.0;
        let result = sim.simulate_trading_day(1);
        assert!(result.n_events > 0, "no events in 60 s");
        assert!(!result.snapshots.is_empty(), "no snapshots");
    }

    #[test]
    fn different_seeds_produce_different_event_counts() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 120.0;
        let r1 = sim.simulate_trading_day(0);
        let r2 = sim.simulate_trading_day(99);
        // Two seeds almost certainly produce different counts.
        assert!(
            r1.n_events != r2.n_events || r1.trades.len() != r2.trades.len(),
            "seeds 0 and 99 produced identical results"
        );
    }

    #[test]
    fn step_returns_none_after_exhaustion() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 30.0;
        sim.reset(7);
        // Drain all events.
        while sim.step().is_some() {}
        assert!(sim.step().is_none());
    }

    #[test]
    fn event_types_in_valid_range() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 60.0;
        sim.reset(3);
        while let Some(ev) = sim.step() {
            assert!(ev.event_type < LOB_DIM, "event_type={} out of range", ev.event_type);
        }
    }

    #[test]
    fn snapshots_have_valid_structure() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 120.0;
        sim.config.snapshot_interval = 10;
        let result = sim.simulate_trading_day(5);
        for (t, snap) in &result.snapshots {
            assert!(*t >= 0.0 && *t <= sim.config.t_max);
            // Snapshot may be empty if book was swept, but timestamp must be set.
            let _ = snap;
        }
    }

    #[test]
    fn best_limit_suppressed_when_spread_is_one_tick() {
        let mut sim = HawkesLobSimulator::default_12d().unwrap();
        sim.config.t_max = 10.0;
        sim.reset(0);

        // Collapse the spread to exactly one tick by inserting synthetic orders.
        let tick = Price::from_ticks(sim.config.tick_size);
        let mid = sim.config.initial_mid;
        // Place bid and ask one tick apart.
        let bid_id = sim.next_id();
        sim.book.insert_resting(Order::new(
            bid_id, Side::Bid, mid, Quantity(10), Timestamp(1), OrderType::Limit,
        ));
        let ask_id = sim.next_id();
        sim.book.insert_resting(Order::new(
            ask_id, Side::Ask, mid + tick, Quantity(10), Timestamp(1), OrderType::Limit,
        ));

        let (bb, _) = sim.book.best_bid().unwrap();
        let (ba, _) = sim.book.best_ask().unwrap();
        assert_eq!(ba - bb, tick, "spread should be exactly 1 tick");

        // A best-buy order would want to go at bb+tick = ba → must be suppressed.
        let trades = sim.submit_limit_best(Side::Bid, Timestamp(2));
        assert!(trades.is_empty(), "order should be suppressed at 1-tick spread");
    }

    #[test]
    fn time_of_day_factor_at_endpoints_equals_peak() {
        let sim = HawkesLobSimulator::default_12d().unwrap();
        let peak = sim.config.uday_peak_factor;
        assert!((sim.time_of_day_factor(0.0) - peak).abs() < 1e-10);
        assert!((sim.time_of_day_factor(sim.config.t_max) - peak).abs() < 1e-10);
    }

    #[test]
    fn time_of_day_factor_at_midday_equals_one() {
        let sim = HawkesLobSimulator::default_12d().unwrap();
        let mid = sim.config.t_max / 2.0;
        assert!((sim.time_of_day_factor(mid) - 1.0).abs() < 1e-10);
    }
}
