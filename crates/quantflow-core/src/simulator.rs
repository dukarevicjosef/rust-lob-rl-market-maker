//! Hawkes-driven limit order book simulator.
//!
//! Maps a 12-dimensional Hawkes process onto LOB events:
//!
//!   Dim  0: Market buy          Dim  1: Market sell
//!   Dim  2: Limit best buy      Dim  3: Limit best sell
//!   Dim  4: Limit deep buy      Dim  5: Limit deep sell
//!   Dim  6: Cancel best buy     Dim  7: Cancel best sell
//!   Dim  8: Cancel deep buy     Dim  9: Cancel deep sell
//!   Dim 10: Modify buy          Dim 11: Modify sell
//!
//! Order sizes follow a log-normal distribution (Gopikrishnan et al., 2000).
//! A U-shape intraday activity profile (Admati & Pfleiderer, 1988) is achieved
//! by thinning the Hawkes event stream with a time-of-day acceptance function:
//!
//!   p_accept(t) = f(t) / peak_factor
//!   f(t) = 1 + (peak_factor − 1) · (2t/T − 1)²

use rand::{Rng, SeedableRng};
use rand::rngs::SmallRng;
use rand_distr::{Distribution, Exp, LogNormal};

use crate::hawkes::kernel::{ExcitationKernel, ExponentialKernel};
use crate::hawkes::process::{HawkesEvent, MultivariateHawkes, ProcessError};
use crate::orderbook::book::{OrderBook, OrderBookSnapshot};
use crate::orderbook::order::{Order, Trade};
use crate::orderbook::types::{OrderId, OrderType, Price, Quantity, Side, Timestamp, PRICE_SCALE};

// ── Dimension index constants ─────────────────────────────────────────────────

pub const LOB_DIM: usize = 12;
pub const DIM_MKT_BUY: usize = 0;
pub const DIM_MKT_SELL: usize = 1;
pub const DIM_LIM_BEST_BUY: usize = 2;
pub const DIM_LIM_BEST_SELL: usize = 3;
pub const DIM_LIM_DEEP_BUY: usize = 4;
pub const DIM_LIM_DEEP_SELL: usize = 5;
pub const DIM_CAN_BEST_BUY: usize = 6;
pub const DIM_CAN_BEST_SELL: usize = 7;
pub const DIM_CAN_DEEP_BUY: usize = 8;
pub const DIM_CAN_DEEP_SELL: usize = 9;
pub const DIM_MOD_BUY: usize = 10;
pub const DIM_MOD_SELL: usize = 11;

// ── Config ────────────────────────────────────────────────────────────────────

/// Configuration for a Hawkes LOB simulation run.
#[derive(Debug, Clone)]
pub struct SimulatorConfig {
    /// One tick in fixed-point price units (e.g., `PRICE_SCALE` = 1.0 per tick).
    pub tick_size: i64,
    /// Initial mid-price.
    pub initial_mid: Price,
    /// Initial half-spread in ticks (each side of mid).
    pub initial_half_spread_ticks: i64,
    /// Emit a book snapshot every this many accepted events.
    pub snapshot_interval: usize,
    /// Log-normal μ for order-size sampling.
    pub lognormal_mu: f64,
    /// Log-normal σ for order-size sampling.
    pub lognormal_sigma: f64,
    /// Peak factor for U-shape intraday profile (≥ 1.0; 1.0 = flat).
    pub uday_peak_factor: f64,
    /// Simulated trading-day length in seconds (e.g. 23400 = 6.5 h).
    pub t_max: f64,
    /// Maximum depth offset in ticks for "deep" limit/cancel orders.
    pub max_deep_offset_ticks: i64,
    /// Number of seed levels to initialise on each side of the book.
    pub seed_levels: usize,
}

impl Default for SimulatorConfig {
    fn default() -> Self {
        SimulatorConfig {
            tick_size: PRICE_SCALE,
            initial_mid: Price::from_f64(100.0),
            initial_half_spread_ticks: 1,
            snapshot_interval: 100,
            lognormal_mu: 3.0,
            lognormal_sigma: 1.0,
            uday_peak_factor: 3.0,
            t_max: 23_400.0,
            max_deep_offset_ticks: 10,
            seed_levels: 5,
        }
    }
}

// ── Output types ──────────────────────────────────────────────────────────────

/// One processed Hawkes event and its effect on the LOB.
#[derive(Debug, Clone)]
pub struct SimulationEvent {
    /// Simulation time in seconds.
    pub sim_time: f64,
    /// Hawkes dimension index (0–11).
    pub event_type: usize,
    /// Trades generated by this event (empty for non-crossing orders).
    pub trades: Vec<Trade>,
    /// Optional depth snapshot (emitted every `snapshot_interval` events).
    pub snapshot: Option<OrderBookSnapshot>,
}

/// Aggregated result of a full trading-day simulation.
#[derive(Debug)]
pub struct SimulationResult {
    /// All trades generated during the day.
    pub trades: Vec<Trade>,
    /// Book snapshots with their simulation timestamps (seconds).
    pub snapshots: Vec<(f64, OrderBookSnapshot)>,
    /// Number of Hawkes events accepted (after U-shape thinning).
    pub n_events: usize,
    /// Total simulation length in seconds.
    pub sim_time: f64,
}

// ── Simulator ─────────────────────────────────────────────────────────────────

/// Hawkes-driven LOB simulator.
///
/// Usage:
/// ```ignore
/// let mut sim = HawkesLobSimulator::default_12d().unwrap();
/// let result = sim.simulate_trading_day(42);
/// ```
pub struct HawkesLobSimulator {
    book: OrderBook,
    hawkes: MultivariateHawkes,
    pub config: SimulatorConfig,
    next_order_id: u64,
    // Pre-simulated Hawkes event stream for the current day.
    pending: Vec<HawkesEvent>,
    cursor: usize,
    // Accumulation buffers.
    all_trades: Vec<Trade>,
    snapshots: Vec<(f64, OrderBookSnapshot)>,
    n_accepted: usize,
    rng: SmallRng,
    // Optional O(D²) fast-path matrices for all-exponential calibrated processes.
    // When set, reset() uses simulate_exp_fast() instead of the O(N²) active-set thinning.
    exp_alpha: Option<Vec<Vec<f64>>>,  // n*[i][j]
    exp_beta:  Option<Vec<Vec<f64>>>,  // β[i][j]
}

impl HawkesLobSimulator {
    /// Construct from an arbitrary Hawkes process and config.
    pub fn new(hawkes: MultivariateHawkes, config: SimulatorConfig) -> Self {
        HawkesLobSimulator {
            book: OrderBook::new(),
            hawkes,
            config,
            next_order_id: 1,
            pending: Vec::new(),
            cursor: 0,
            all_trades: Vec::new(),
            snapshots: Vec::new(),
            n_accepted: 0,
            rng: SmallRng::seed_from_u64(0),
            exp_alpha: None,
            exp_beta: None,
        }
    }

    /// Construct from calibrated per-dimension Hawkes parameters.
    ///
    /// Parameter conventions match the Python MLE output:
    ///   - `baselines[i]`      = μ_i  (baseline rate, events/s)
    ///   - `alpha_matrix[i][j]` = α_ij / β_ij  (branching-ratio contribution, Rust n* convention)
    ///   - `beta_matrix[i][j]`  = β_ij  (decay rate, 1/s)
    ///
    /// The Python MLE parameterises φ(t) = α·exp(−β·t), so ∫φ = α/β.
    /// The Rust `ExponentialKernel` parameterises φ(t) = n*·β·exp(−β·t), so ∫φ = n*.
    /// Hence the caller must pass `n*_ij = α_py_ij / β_py_ij`.
    ///
    /// Values are clamped to safe ranges:
    ///   n* ∈ [0, 0.999] (kernel stability),  β ≥ 0.01 (avoid division by zero).
    pub fn from_calibrated(
        baselines:    Vec<f64>,
        alpha_matrix: Vec<Vec<f64>>,   // alpha_matrix[i][j] = n* from source j to target i
        beta_matrix:  Vec<Vec<f64>>,   // beta_matrix[i][j]  = decay rate
        config:       SimulatorConfig,
    ) -> Result<Self, ProcessError> {
        let d = baselines.len();
        let kernels: Vec<Vec<Box<dyn ExcitationKernel + Send + Sync>>> = (0..d)
            .map(|i| {
                (0..d)
                    .map(|j| -> Box<dyn ExcitationKernel + Send + Sync> {
                        let a = alpha_matrix[i][j].clamp(0.0, 0.999);
                        let b = beta_matrix[i][j].max(0.01);
                        Box::new(
                            ExponentialKernel::new(a, b)
                                .unwrap_or_else(|_| ExponentialKernel::new(0.0, 1.0).unwrap()),
                        )
                    })
                    .collect()
            })
            .collect();

        // Store clamped matrices for the O(D²) fast-path thinning.
        let clamped_alpha: Vec<Vec<f64>> = alpha_matrix.iter()
            .map(|row| row.iter().map(|&a| a.clamp(0.0, 0.999)).collect())
            .collect();
        let clamped_beta: Vec<Vec<f64>> = beta_matrix.iter()
            .map(|row| row.iter().map(|&b| b.max(0.01)).collect())
            .collect();

        let hawkes = MultivariateHawkes::new(baselines, kernels)?;
        let mut sim = HawkesLobSimulator::new(hawkes, config);
        sim.exp_alpha = Some(clamped_alpha);
        sim.exp_beta  = Some(clamped_beta);
        Ok(sim)
    }

    /// Build a 12-dimensional LOB simulator with calibrated default parameters.
    ///
    /// Background rates follow the empirical ordering:
    ///   market < modify < cancel deep < cancel best < limit deep < limit best
    ///
    /// The excitation matrix uses:
    ///   - Self-excitation      α = 0.20
    ///   - Symmetric-side       α = 0.10 (same event class, opposite side)
    ///   - Cross-class          α = 0.03
    ///   Row sums ≈ 0.20 + 0.10 + 10×0.03 = 0.60 < 1 → process is stable.
    pub fn default_12d() -> Result<Self, ProcessError> {
        let baselines = vec![
            0.5, 0.5, // market buy / sell
            3.0, 3.0, // limit best buy / sell
            2.0, 2.0, // limit deep buy / sell
            1.5, 1.5, // cancel best buy / sell
            1.0, 1.0, // cancel deep buy / sell
            0.5, 0.5, // modify buy / sell
        ];

        let beta = 3.0_f64;
        let alpha = |i: usize, j: usize| -> f64 {
            if i == j {
                0.20
            } else if i / 2 == j / 2 {
                // Same event class (buy vs sell pair), e.g. 0↔1, 2↔3
                0.10
            } else {
                0.03
            }
        };

        let kernels: Vec<Vec<Box<dyn ExcitationKernel + Send + Sync>>> = (0..LOB_DIM)
            .map(|i| {
                (0..LOB_DIM)
                    .map(|j| -> Box<dyn ExcitationKernel + Send + Sync> {
                        Box::new(ExponentialKernel::new(alpha(i, j), beta).unwrap())
                    })
                    .collect()
            })
            .collect();

        let hawkes = MultivariateHawkes::new(baselines, kernels)?;
        Ok(HawkesLobSimulator::new(hawkes, SimulatorConfig::default()))
    }

    /// Reset simulator state: re-simulate a new Hawkes event stream and
    /// reinitialise the order book.
    pub fn reset(&mut self, seed: u64) {
        self.rng = SmallRng::seed_from_u64(seed);
        self.book = OrderBook::new();
        self.next_order_id = 1;
        self.all_trades.clear();
        self.snapshots.clear();
        self.n_accepted = 0;
        self.pending = match (&self.exp_alpha, &self.exp_beta) {
            (Some(alpha), Some(beta)) => {
                // O(D²) per step — ~1000× faster than the generic O(N·D²) active-set scan
                // for calibrated BTC/USDT params where slow-decaying kernels keep the
                // active set large (Bacry et al. 2015, recursive intensity update).
                simulate_exp_fast(&self.hawkes.baselines, alpha, beta, self.config.t_max, seed)
            }
            _ => self.hawkes.simulate(self.config.t_max, seed),
        };
        self.cursor = 0;
        self.initialize_book();
    }

    /// Advance by one accepted Hawkes event.
    ///
    /// Returns `None` when the pre-simulated event stream is exhausted.
    /// Applies U-shape thinning: events near open and close are more likely
    /// to be accepted, creating the empirical U-shaped intraday pattern.
    pub fn step(&mut self) -> Option<SimulationEvent> {
        loop {
            if self.cursor >= self.pending.len() {
                return None;
            }
            let event = self.pending[self.cursor].clone();
            self.cursor += 1;

            // U-shape acceptance: p(t) = f(t) / peak_factor ∈ (0, 1].
            let p_accept = self.time_of_day_factor(event.time) / self.config.uday_peak_factor;
            if self.rng.gen::<f64>() > p_accept {
                continue;
            }

            let ts = Timestamp((event.time * 1e9) as u64);
            let trades = self.apply_event(event.event_type, ts);
            self.all_trades.extend_from_slice(&trades);
            self.n_accepted += 1;

            let snapshot = if self.n_accepted % self.config.snapshot_interval == 0 {
                let snap = self.book.snapshot(10);
                self.snapshots.push((event.time, snap.clone()));
                Some(snap)
            } else {
                None
            };

            return Some(SimulationEvent {
                sim_time: event.time,
                event_type: event.event_type,
                trades,
                snapshot,
            });
        }
    }

    /// Simulate a full trading day from scratch.
    pub fn simulate_trading_day(&mut self, seed: u64) -> SimulationResult {
        self.reset(seed);
        while self.step().is_some() {}
        SimulationResult {
            trades: self.all_trades.clone(),
            snapshots: self.snapshots.clone(),
            n_events: self.n_accepted,
            sim_time: self.config.t_max,
        }
    }

    // ── Internal helpers ──────────────────────────────────────────────────────

    /// f(t) = 1 + (peak − 1)·(2t/T − 1)²
    /// Equals `peak_factor` at t=0 and t=T; equals 1.0 at t=T/2.
    fn time_of_day_factor(&self, t: f64) -> f64 {
        let u = t / self.config.t_max;
        1.0 + (self.config.uday_peak_factor - 1.0) * (2.0 * u - 1.0).powi(2)
    }

    fn next_id(&mut self) -> OrderId {
        let id = self.next_order_id;
        self.next_order_id += 1;
        OrderId(id)
    }

    fn sample_qty(&mut self) -> Quantity {
        let ln = LogNormal::new(self.config.lognormal_mu, self.config.lognormal_sigma).unwrap();
        let raw = ln.sample(&mut self.rng);
        Quantity((raw.round() as u64).max(1))
    }

    fn one_tick(&self) -> Price {
        Price::from_ticks(self.config.tick_size)
    }

    /// Seed the book with `seed_levels` levels on each side around `initial_mid`.
    fn initialize_book(&mut self) {
        let tick = Price::from_ticks(self.config.tick_size);
        let half = Price::from_ticks(self.config.initial_half_spread_ticks * self.config.tick_size);
        let best_bid = self.config.initial_mid - half;
        let best_ask = self.config.initial_mid + half;

        for lvl in 0..self.config.seed_levels as i64 {
            let bid_price = best_bid - Price::from_ticks(lvl * self.config.tick_size);
            let ask_price = best_ask + Price::from_ticks(lvl * self.config.tick_size);
            let qty = Quantity(100 + self.rng.gen_range(0_u64..200));

            let bid_id = self.next_id();
            self.book.insert_resting(Order::new(
                bid_id, Side::Bid, bid_price, qty, Timestamp(0), OrderType::Limit,
            ));
            let ask_id = self.next_id();
            self.book.insert_resting(Order::new(
                ask_id, Side::Ask, ask_price, qty, Timestamp(0), OrderType::Limit,
            ));
        }
        let _ = tick; // used indirectly through price arithmetic above
    }

    /// Dispatch a Hawkes event to the appropriate LOB action.
    fn apply_event(&mut self, event_type: usize, ts: Timestamp) -> Vec<Trade> {
        match event_type {
            DIM_MKT_BUY => self.submit_market(Side::Bid, ts),
            DIM_MKT_SELL => self.submit_market(Side::Ask, ts),
            DIM_LIM_BEST_BUY => self.submit_limit_best(Side::Bid, ts),
            DIM_LIM_BEST_SELL => self.submit_limit_best(Side::Ask, ts),
            DIM_LIM_DEEP_BUY => self.submit_limit_deep(Side::Bid, ts),
            DIM_LIM_DEEP_SELL => self.submit_limit_deep(Side::Ask, ts),
            DIM_CAN_BEST_BUY => {
                self.cancel_best(Side::Bid);
                vec![]
            }
            DIM_CAN_BEST_SELL => {
                self.cancel_best(Side::Ask);
                vec![]
            }
            DIM_CAN_DEEP_BUY => {
                self.cancel_deep(Side::Bid);
                vec![]
            }
            DIM_CAN_DEEP_SELL => {
                self.cancel_deep(Side::Ask);
                vec![]
            }
            DIM_MOD_BUY => {
                self.modify_random(Side::Bid);
                vec![]
            }
            DIM_MOD_SELL => {
                self.modify_random(Side::Ask);
                vec![]
            }
            _ => vec![],
        }
    }

    // ── Order submission helpers ──────────────────────────────────────────────

    fn submit_market(&mut self, side: Side, ts: Timestamp) -> Vec<Trade> {
        let price = match side {
            // Aggressive prices guaranteed to cross all resting liquidity.
            Side::Bid => Price::from_ticks(i64::MAX / 4),
            Side::Ask => Price::from_ticks(1),
        };
        let qty = self.sample_qty();
        let order = Order::new(self.next_id(), side, price, qty, ts, OrderType::Market);
        self.book.add_limit_order(order).trades
    }

    /// Place a limit order one tick inside the current spread.
    /// Suppressed if the book is one-sided and the new price would cross.
    fn submit_limit_best(&mut self, side: Side, ts: Timestamp) -> Vec<Trade> {
        let tick = self.one_tick();
        let price = match side {
            Side::Bid => {
                let candidate = self.book.best_bid()
                    .map(|(p, _)| p + tick)
                    .unwrap_or_else(|| {
                        self.config.initial_mid
                            - Price::from_ticks(
                                self.config.initial_half_spread_ticks * self.config.tick_size,
                            )
                    });
                // Suppress if it would cross the ask.
                if let Some((ba, _)) = self.book.best_ask() {
                    if candidate >= ba {
                        return vec![];
                    }
                }
                candidate
            }
            Side::Ask => {
                let candidate = self.book.best_ask()
                    .map(|(p, _)| p - tick)
                    .unwrap_or_else(|| {
                        self.config.initial_mid
                            + Price::from_ticks(
                                self.config.initial_half_spread_ticks * self.config.tick_size,
                            )
                    });
                if let Some((bb, _)) = self.book.best_bid() {
                    if candidate <= bb {
                        return vec![];
                    }
                }
                candidate
            }
        };
        let qty = self.sample_qty();
        let order = Order::new(self.next_id(), side, price, qty, ts, OrderType::Limit);
        self.book.add_limit_order(order).trades
    }

    /// Place a limit order 2–max_deep ticks behind the best.
    fn submit_limit_deep(&mut self, side: Side, ts: Timestamp) -> Vec<Trade> {
        let offset_ticks =
            self.rng.gen_range(2_i64..=self.config.max_deep_offset_ticks.max(2));
        let offset = Price::from_ticks(offset_ticks * self.config.tick_size);
        let price = match side {
            Side::Bid => {
                self.book.best_bid().map(|(p, _)| p).unwrap_or(self.config.initial_mid) - offset
            }
            Side::Ask => {
                self.book.best_ask().map(|(p, _)| p).unwrap_or(self.config.initial_mid) + offset
            }
        };
        let qty = self.sample_qty();
        let order = Order::new(self.next_id(), side, price, qty, ts, OrderType::Limit);
        self.book.add_limit_order(order).trades
    }

    // ── Cancel / modify helpers ───────────────────────────────────────────────

    /// Cancel the time-priority (front) order at the best price level.
    fn cancel_best(&mut self, side: Side) {
        let id = match side {
            Side::Bid => self
                .book
                .bids
                .iter()
                .next()
                .and_then(|(_, lvl)| lvl.front())
                .map(|o| o.id),
            Side::Ask => self
                .book
                .asks
                .iter()
                .next()
                .and_then(|(_, lvl)| lvl.front())
                .map(|o| o.id),
        };
        if let Some(id) = id {
            self.book.cancel_order(id);
        }
    }

    /// Cancel a random order from levels 2+ (deep side of the book).
    fn cancel_deep(&mut self, side: Side) {
        // Collect IDs first (immutable borrow), then cancel (mutable borrow).
        let ids: Vec<OrderId> = match side {
            Side::Bid => self
                .book
                .bids
                .iter()
                .skip(1)
                .take(5)
                .flat_map(|(_, lvl)| lvl.iter().map(|o| o.id))
                .collect(),
            Side::Ask => self
                .book
                .asks
                .iter()
                .skip(1)
                .take(5)
                .flat_map(|(_, lvl)| lvl.iter().map(|o| o.id))
                .collect(),
        };
        if !ids.is_empty() {
            let idx = self.rng.gen_range(0..ids.len());
            self.book.cancel_order(ids[idx]);
        }
    }

    /// Reduce a random resting order's quantity by half.
    fn modify_random(&mut self, side: Side) {
        let candidates: Vec<(OrderId, Quantity)> = match side {
            Side::Bid => self
                .book
                .bids
                .values()
                .flat_map(|lvl| lvl.iter().map(|o| (o.id, o.quantity)))
                .take(20)
                .collect(),
            Side::Ask => self
                .book
                .asks
                .values()
                .flat_map(|lvl| lvl.iter().map(|o| (o.id, o.quantity)))
                .take(20)
                .collect(),
        };
        if !candidates.is_empty() {
            let idx = self.rng.gen_range(0..candidates.len());
            let (id, qty) = candidates[idx];
            let new_qty = Quantity((qty.0 / 2).max(1));
            if new_qty < qty {
                self.book.modify_order(id, new_qty);
            }
        }
    }

    // ── Strategy interface ────────────────────────────────────────────────────

    /// Current mid-price as a float. Returns `None` if the book is one-sided.
    pub fn mid_price(&self) -> Option<f64> {
        self.book.mid_price().map(|p| p.to_f64())
    }

    /// Place a resting limit order on behalf of an external agent.
    ///
    /// Returns the assigned `OrderId` so the caller can track fills and cancel.
    /// The order is inserted directly without going through the matching engine;
    /// the strategy is responsible for ensuring bid < best_ask and ask > best_bid.
    pub fn place_limit_order(&mut self, side: Side, price: Price, qty: Quantity) -> OrderId {
        let id = self.next_id();
        self.book.insert_resting(Order::new(id, side, price, qty, Timestamp(0), OrderType::Limit));
        id
    }

    /// Cancel an agent order by ID. No-op if the ID is not found.
    pub fn cancel_agent_order(&mut self, id: OrderId) {
        self.book.cancel_order(id);
    }

    /// Read-only access to the order book (for strategy observation).
    pub fn book(&self) -> &OrderBook {
        &self.book
    }
}

// ── O(D²) exponential-kernel fast-path ───────────────────────────────────────

/// Ogata thinning for a multivariate Hawkes process whose kernels are ALL
/// exponential.  Uses the recursive intensity update
///
///   R[i][j](t + dt) = R[i][j](t) · exp(−β_ij · dt)
///
/// to advance the running excitation in O(D²) per candidate event instead of
/// scanning the entire active-event history (O(N · D²) in the generic path).
///
/// Complexity: O(N_candidates · D²).  For a stable process,
/// N_candidates ≈ N_accepted / acceptance_rate, so the total cost is
/// O(t_max · λ̄ · D²) — linear in t_max, independent of history depth.
///
/// Reference: Bacry, Mastromatteo & Muzy (2015), §3 — recursive form of the
/// conditional intensity for exponential kernels.
fn simulate_exp_fast(
    mu:    &[f64],
    alpha: &[Vec<f64>],   // n*[i][j]  (branching ratio, ≤ 0.999)
    beta:  &[Vec<f64>],   // β[i][j]   (decay rate, > 0)
    t_max: f64,
    seed:  u64,
) -> Vec<HawkesEvent> {
    let d = mu.len();
    let mut rng = SmallRng::seed_from_u64(seed);

    // R[i][j] = Σ_{accepted k of type j} α_ij · β_ij · exp(−β_ij · (t − t_k))
    //         = running sum of φ_ij(t − t_k) for all past type-j events.
    let mut r = vec![vec![0.0_f64; d]; d];
    let mut t = 0.0_f64;

    // λ* — upper bound on total intensity; initially Σ μ_i (no history yet).
    let mut lambda_star: f64 = mu.iter().sum();

    let mut history: Vec<HawkesEvent> = Vec::new();

    loop {
        if lambda_star <= 0.0 {
            break;
        }

        // Draw candidate inter-arrival dt ~ Exp(λ*).
        let dt: f64 = rng.sample(Exp::new(lambda_star).unwrap());
        let t_cand = t + dt;
        if t_cand > t_max {
            break;
        }

        // ── Recursive decay: advance R from t to t_cand ──────────────────────
        for i in 0..d {
            for j in 0..d {
                r[i][j] *= (-beta[i][j] * dt).exp();
            }
        }
        t = t_cand;

        // ── Intensity at t_cand ───────────────────────────────────────────────
        let mut lambda_cand_total = 0.0_f64;
        for i in 0..d {
            let li: f64 = mu[i] + r[i].iter().sum::<f64>();
            lambda_cand_total += li;
        }

        // ── Thinning acceptance test (Ogata 1981, §2) ────────────────────────
        if rng.gen::<f64>() * lambda_star <= lambda_cand_total {
            // ── Accept ──────────────────────────────────────────────────────
            // Sample event type ∝ λ_i(t_cand).
            let threshold = rng.gen::<f64>() * lambda_cand_total;
            let mut cumsum = 0.0_f64;
            let mut event_type = d - 1;
            for i in 0..d {
                cumsum += mu[i] + r[i].iter().sum::<f64>();
                if threshold < cumsum {
                    event_type = i;
                    break;
                }
            }

            // Add φ_ij(0) = α_ij · β_ij to R[i][event_type] for all i.
            let mut excitation_sum = 0.0_f64;
            for i in 0..d {
                let contrib = alpha[i][event_type] * beta[i][event_type];
                r[i][event_type] += contrib;
                excitation_sum += contrib;
            }

            // New λ* = λ(t_cand⁺) = current intensity + new excitation.
            lambda_star = lambda_cand_total + excitation_sum;

            history.push(HawkesEvent { time: t_cand, event_type });
        } else {
            // ── Reject ───────────────────────────────────────────────────────
            // Tighten the upper bound to the current (lower) intensity.
            lambda_star = lambda_cand_total;
        }
    }

    history
}

