# ── synthetic Hawkes simulation ───────────────────────────────────────────────


def simulate_hawkes_1d(
    mu:    float,
    alpha: float,
    beta:  float,
    T:     float,
    seed:  int = 0,
) -> np.ndarray:
    """
    Simulate a 1-D Hawkes process via Ogata's thinning algorithm.
    Returns sorted event times in [0, T).
    """
    rng    = np.random.default_rng(seed)
    events = []
    t      = 0.0

    # λ*(t) = mu + alpha * Σ_{s<t} exp(-beta*(t-s))
    running_sum = 0.0   # Σ exp(-beta*(t-s)) at current t

    while t < T:
        # Upper bound on intensity at current t
        lam_bar = mu + alpha * running_sum + alpha   # +alpha for potential new event

        # Draw next candidate time
        dt = rng.exponential(1.0 / lam_bar)
        t += dt
        if t >= T:
            break

        # Update running sum for new t
        running_sum *= np.exp(-beta * dt)

        # Actual intensity at t
        lam_t = mu + alpha * running_sum

        # Accept / reject
        if rng.uniform() <= lam_t / lam_bar:
            events.append(t)
            running_sum += 1.0  # include this event in future sums

    return np.array(events, dtype=np.float64)


def simulate_hawkes_2d(
    mu:    np.ndarray,      # (2,)
    alpha: np.ndarray,      # (2, 2)
    beta:  np.ndarray,      # (2, 2)
    T:     float,
    seed:  int = 0,
) -> list[np.ndarray]:
    """
    Simulate a 2-D Hawkes process via Ogata's thinning.
    Returns [times_dim0, times_dim1].
    """
    rng    = np.random.default_rng(seed)
    events: list[list[float]] = [[], []]
    t      = 0.0
    R      = np.zeros((2, 2))   # R[i, j] = Σ_{s<t, dim=j} exp(-beta[i,j]*(t-s))

    while t < T:
        # Upper bound: evaluate each dim's max intensity
        lam_upper = np.zeros(2)
        for i in range(2):
            lam_upper[i] = mu[i] + np.sum(alpha[i] * R[i]) + np.sum(alpha[i])
        lam_bar = np.sum(lam_upper) + 1e-9

        dt = rng.exponential(1.0 / lam_bar)
        t += dt
        if t >= T:
            break

        # Update R for dt
        for i in range(2):
            R[i] *= np.exp(-beta[i] * dt)

        # Actual intensities
        lam = np.array([mu[i] + np.sum(alpha[i] * R[i]) for i in range(2)])

        # Accept / reject
        u = rng.uniform() * lam_bar
        if u < lam[0]:
            events[0].append(t)
            for i in range(2):
                R[i, 0] += 1.0
        elif u < lam[0] + lam[1]:
            events[1].append(t)
            for i in range(2):
                R[i, 1] += 1.0

    return [np.array(e, dtype=np.float64) for e in events]


# ── helpers ───────────────────────────────────────────────────────────────────


def _make_event_data_stub(all_times: list[np.ndarray], T: float):
    """Minimal duck-type that satisfies HawkesMLE.calibrate() interface."""
    class _Stub:
        n_dims       = len(all_times)
        total_events = sum(len(t) for t in all_times)
        t_span       = T
        source_path  = "<synthetic>"
        dim_stats    = [{"dim": i, "active": len(all_times[i]) >= 50} for i in range(len(all_times))]

        @property
        def active_dims(self):
            return [i for i, s in enumerate(self.dim_stats) if s["active"]]

        def times_for_dim(self, d):
            return all_times[d]

    return _Stub()


